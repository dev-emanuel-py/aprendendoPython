"""
Tipo None

O tipo de dado none em python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porém, falar que é um tipo sem tipo é mais apropriado.

Obs: O tipo None é sempre especificado com letra maiuscula.

Certo: None
Errado: none

Quando utilizamos ?
- Podemos utilizar o None quando queremos criar uma variavel e inicializa-la com um tipo sem tipo, antes
de recerbermos um valor final.

Obs: o tipo None em python é sempre considerado como falso
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))
